<script>
    $( '#formmail' ).submit(function ( e ) {




        var data = {
            'name': $('#name').val(),
            'email': $('#email').val(),
            'subject': $('#subject').val(),
            'message' : $('#message').val(),
            "_token": "{{ csrf_token() }}",
        };
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        // POST data to the php file
        $.ajax({
            url: '{{route('contact')}}',
            data: data,
            type: 'POST',
            success: function (data) {
                // For Notification
                document.getElementById("formmail").reset();
                var $alertDiv = $(".mailResponse");
                $alertDiv.show();
                $alertDiv.find('.alert').removeClass('alert-danger alert-success');
                $alertDiv.find('.mailResponseText').text("");
                if (data.error) {
                    $alertDiv.find('.alert').addClass('alert-danger');
                    $alertDiv.find('.mailResponseText').text(data.message);
                } else {
                    $alertDiv.find('.alert').addClass('alert-success');
                    $alertDiv.find('.mailResponseText').text('Email Sent');
                }
            }
        });
        e.preventDefault();
    });
</script>

